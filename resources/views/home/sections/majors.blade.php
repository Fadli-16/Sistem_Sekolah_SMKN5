    document.addEventListener('DOMContentLoaded', function() {
        try {
            // Handle tab switching to ensure proper layout rendering
            const majorTabs = document.querySelectorAll('#majorsTabs button');
            if (majorTabs) {
                majorTabs.forEach(tab => {
                    tab.addEventListener('shown.bs.tab', function() {
                        // Force redraw of current tab content to ensure proper layout
                        const tabContent = document.querySelector(this.dataset.bsTarget);
                        if (tabContent) {
                            tabContent.style.display = 'none';
                            setTimeout(() => {
                                tabContent.style.display = '';
                            }, 5);
                        }
                    });
                });
            }
        } catch (error) {
            console.error('Error in majors tab handling:', error);
        }
    });
