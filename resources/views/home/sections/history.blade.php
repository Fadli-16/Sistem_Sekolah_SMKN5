<script>
document.addEventListener('DOMContentLoaded', function() {
    // Year navigation functionality
    const yearButtons = document.querySelectorAll('.history-year');
    const timelineItems = document.querySelectorAll('.timeline-item');
    
    // Initialize timeline
    updateTimeline('1952');
    
    // Add click events to year buttons
    yearButtons.forEach(button => {
        button.addEventListener('click', function() {
            const year = this.getAttribute('data-year');
            updateTimeline(year);
            
            // Animate to the timeline item
            const targetItem = document.getElementById('year-' + year);
            if (targetItem) {
                const offset = window.innerWidth > 991 ? 100 : 50;
                const targetPosition = targetItem.getBoundingClientRect().top + window.pageYOffset - offset;
                
                window.scrollTo({
                    top: targetPosition,
                    behavior: 'smooth'
                });
            }
        });
    });
    
    // Update timeline based on selected year
    function updateTimeline(year) {
        // Remove active class from all buttons and items
        yearButtons.forEach(btn => btn.classList.remove('active'));
        timelineItems.forEach(item => item.classList.remove('active'));
        
        // Add active class to selected year button and item
        document.querySelector(`.history-year[data-year="${year}"]`).classList.add('active');
        document.getElementById('year-' + year).classList.add('active');
    }
    
    // Scroll spy functionality
    window.addEventListener('scroll', function() {
        const scrollPosition = window.scrollY + window.innerHeight / 2;
        
        // Check which timeline item is in view
        timelineItems.forEach(item => {
            const itemTop = item.getBoundingClientRect().top + window.pageYOffset;
            const itemBottom = itemTop + item.offsetHeight;
            const year = item.id.replace('year-', '');
            
            if (scrollPosition >= itemTop && scrollPosition < itemBottom) {
                yearButtons.forEach(btn => btn.classList.remove('active'));
                document.querySelector(`.history-year[data-year="${year}"]`).classList.add('active');
            }
        });
    });
});
</script>